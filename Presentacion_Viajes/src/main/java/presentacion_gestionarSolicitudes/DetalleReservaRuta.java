    private void btnRechazarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechazarActionPerformed
        try {
            controlPantallas.rechazarSolicitud(reserva);
            JOptionPane.showMessageDialog(this, "Reserva rechazada. Se notificos al pasajero.", "Reserva Rechazada", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al rechazar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        controlPantallas.mostrarGestionSolicitudes(reserva.getViaje().getId());
    }//GEN-LAST:event_btnRechazarActionPerformed

    private void btnEnviarPropuestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarPropuestaActionPerformed
        try {
            String precioStr = txtPrecioPropuesto.getText().trim();

            if (precioStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El precio es obligatorio.", "Campo Requerido", JOptionPane.WARNING_MESSAGE);
                return;
            }

            double nuevoPrecio;
            try {
                nuevoPrecio = Double.parseDouble(precioStr);
                if (nuevoPrecio <= 0) {
                    JOptionPane.showMessageDialog(this, "El precio debe ser un número positivo.", "Dato Inválido", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El precio debe ser un número válido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Crear el DTO con el precio propuesto
            ReservacionDTO reservacionPropuesta = new ReservacionDTO();
            reservacionPropuesta.setId(reserva.getId()); 
            reservacionPropuesta.setViaje(reserva.getViaje());
            reservacionPropuesta.setParada(reserva.getParada()); 
            reservacionPropuesta.setPasajero(reserva.getPasajero());

            reservacionPropuesta.setPrecioTotal(nuevoPrecio); // Nuevo precio propuesto

            //Llama a la capa de control para guardar la propuesta
            controlPantallas.proponerPrecio(reservacionPropuesta);

            JOptionPane.showMessageDialog(this,
                    "Propuesta de precio enviada al pasajero.\nLa reserva será confirmada automáticamente si el pasajero acepta.",
                    "Propuesta Enviada",
                    JOptionPane.INFORMATION_MESSAGE);

            // 3. Regresa a la pantalla de gestión de solicitudes
            controlPantallas.mostrarGestionSolicitudes(reserva.getViaje().getId());

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this,
                    "Error al proponer precio: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnEnviarPropuestaActionPerformed

