     // MÃ©todo para dibujar la lista de viajes en el panel contenedor
    private void mostrarViajes(List<ViajeDTO> listaViajes) {
        try {
            JPanel panelInterno = new JPanel();
            panelInterno.setBackground(Color.WHITE);
            panelInterno.setLayout(new BoxLayout(panelInterno, BoxLayout.Y_AXIS));

            int rowHeight = 50;
            int preferredHeight = Math.max(250, listaViajes.size() * rowHeight);

            panelInterno.setPreferredSize(new Dimension(740, preferredHeight));

            if (listaViajes.isEmpty()) {
                panelInterno.add(new JLabel("No tienes viajes activos para gestionar solicitudes."));
            } else {
                for (ViajeDTO viaje : listaViajes) {
                    JPanel panelElemento = new JPanel();
                panelElemento.setLayout(new java.awt.FlowLayout(java.awt.FlowLayout.CENTER, 0, 0));
                panelElemento.setPreferredSize(new Dimension(720, rowHeight));
                panelElemento.setMaximumSize(new Dimension(720, rowHeight));
                panelElemento.setBorder(BorderFactory.createMatteBorder(0, 0, 1, 0, new Color(200, 200, 200)));
                panelElemento.setBackground(new Color(255, 255, 255)); 

                JButton btnInfo = new JButton();
                btnInfo.setFont(new Font("Dialog", Font.PLAIN, 14)); 
                btnInfo.setBackground(Color.WHITE);
                btnInfo.setForeground(Color.BLACK); 
                btnInfo.setHorizontalAlignment(SwingConstants.LEFT);
                btnInfo.setPreferredSize(new Dimension(700, 40)); 
                btnInfo.setBorder(BorderFactory.createEmptyBorder(5, 10, 5, 10)); 
                
                String infoTexto = String.format("Origen: %s | Destino: %s | Fecha: %s",
                                                viaje.getOrigen(), viaje.getDestino(), viaje.getFecha());
                btnInfo.setText(infoTexto);
                    btnInfo.addActionListener(e -> {
                        controlPantallas.mostrarGestionSolicitudes(viaje.getId());
                    });

                    panelElemento.add(btnInfo);
                    panelInterno.add(panelElemento);
                }
            }
           

            JScrollPane scrollPane = new JScrollPane(panelInterno);
            scrollPane.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_AS_NEEDED);
            scrollPane.setHorizontalScrollBarPolicy(JScrollPane.HORIZONTAL_SCROLLBAR_NEVER);
            scrollPane.setPreferredSize(new Dimension(750, 400));
            scrollPane.setBackground(Color.WHITE);
            
            panelContenedorViajes.removeAll();
            panelContenedorViajes.setLayout(new java.awt.FlowLayout(java.awt.FlowLayout.CENTER));
            panelContenedorViajes.add(scrollPane);
            panelContenedorViajes.revalidate();
            panelContenedorViajes.repaint();

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this,
                "Error al cargar viajes: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }
       
