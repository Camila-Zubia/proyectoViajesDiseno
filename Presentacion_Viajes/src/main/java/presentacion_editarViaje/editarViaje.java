    public editarViaje(IControlPantallas controlPantallas) {
        this.controlPantallas = controlPantallas;

        this.viajeActual = controlPantallas.obtenerViajeParaEdicion();

        if (this.viajeActual == null) {
            // Si no hay viaje, muestra el error y regresa al menú de conductor
            JOptionPane.showMessageDialog(null, "No hay un viaje seleccionado para editar. Regresando al menu.", "Error", JOptionPane.ERROR_MESSAGE);
            this.controlPantallas.mostrarMenuConductor();
            this.viajeActual = new ViajeDTO(); 
        } else {
            // Si el viaje existe, inicializa la interfaz y carga los datos
            initComponents();
            cargarDatosViaje();
            mostrarParadas();
        }
    }
    
    private void cargarDatosViaje() {
        origenTField.setText(viajeActual.getOrigen());
        destinoTField.setText(viajeActual.getDestino());
        PrecioTextField.setText(String.valueOf(viajeActual.getPrecioTotal()));
        fechaHora.getDatePicker().setDate(viajeActual.getFecha());
        fechaHora.getTimePicker().setTime(viajeActual.getHora());
        
        // El origen no se debe poder editar
        origenTField.setEditable(false);
    }
    
     // metodo para mostrar las paradas en el scroll
    private void mostrarParadas() {
        if (viajeActual == null || viajeActual.getParadas() == null) return;
        
        jPanelParadas.removeAll();
        jPanelParadas.setLayout(new BoxLayout(jPanelParadas, BoxLayout.Y_AXIS));
        
        for (int i = 0; i < viajeActual.getParadas().size(); i++) {
            ParadaDTO parada = viajeActual.getParadas().get(i);
            
            JPanel panelInfo = new JPanel(new FlowLayout(FlowLayout.LEFT, 5, 5));
            panelInfo.setBackground(new Color(240, 240, 240));
            panelInfo.setPreferredSize(new Dimension(390, 40));
            panelInfo.setMaximumSize(new Dimension(390, 40));
            panelInfo.setBorder(BorderFactory.createMatteBorder(0, 0, 1, 0, Color.LIGHT_GRAY));

            JLabel lblIndex = new JLabel("#" + (i + 1) + ": ");
            lblIndex.setFont(new Font("Segoe UI", Font.BOLD, 12));
            
            JLabel lblInfo = new JLabel(parada.getDirección() + " ($" + parada.getPrecio() + ")");
            
            panelInfo.add(lblIndex);
            panelInfo.add(lblInfo);
            
            jPanelParadas.add(panelInfo);
        }
        
        jPanelParadas.revalidate();
        jPanelParadas.repaint();
    }

