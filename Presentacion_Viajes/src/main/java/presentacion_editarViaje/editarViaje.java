    private void guardarCambiosViajeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarCambiosViajeBtnActionPerformed
        // Validar y guardar los datos del viaje antes de continuar

        String origen = origenTField.getText().trim();
        String destino = destinoTField.getText().trim();
        LocalDate fecha = fechaHora.getDatePicker().getDate();
        LocalTime hora = fechaHora.getTimePicker().getTime();
        String precioStr = PrecioTextField.getText().trim();

        double precioBase;
        if (precioStr.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Por favor ingrese un precio.",
                "Campo Requerido",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            precioBase = Double.parseDouble(precioStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "El precio debe ser un numero valido.",
                "Error de Formato",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (origen.isEmpty() || destino.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Por favor complete todos los campos",
                "Campos incompletos",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        //validacion, origen y destino iguales
        if (origen.equalsIgnoreCase(destino)) {
            JOptionPane.showMessageDialog(this, "El Origen y el Destino no pueden ser la misma ubicacion.", "Dato Invalido", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // validacion de tipo de dato origen y destino solo con texto
        if (origen.matches(".*\\d.*") || destino.matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(this, "El Origen y Destino no deben contener números. Ingrese solo texto.", "Dato Invalido", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validar fecha y hora
        if (fechaHora.getDateTimeStrict() == null) {
            JOptionPane.showMessageDialog(this,
                "Por favor seleccione una fecha y hora válida",
                "Fecha inválida",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validar que la fecha no sea en el pasado
        if (fechaHora.getDateTimeStrict().isBefore(java.time.LocalDateTime.now())) {
            JOptionPane.showMessageDialog(this,
                "La fecha y hora del viaje no puede ser en el pasado",
                "Fecha inválida",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Crear DTO modificado para guardar los datos generales
        ViajeDTO viajeModificado = new ViajeDTO();
        viajeModificado.setId(viajeActual.getId());
        viajeModificado.setOrigen(origen); 
        viajeModificado.setDestino(destino);
        viajeModificado.setFecha(fecha);
        viajeModificado.setHora(hora);
        viajeModificado.setPrecioTotal(precioBase);
        
        // Llamar al Control para guardar los cambios generales
        controlPantallas.guardarCambiosViaje(viajeModificado);

        //  Mostrar el mensaje de confirmacion solicitado
        JOptionPane.showMessageDialog(this,
                "El viaje ha sido modificado con exito!!\nPor favor presione Aceptar para volver al menu principal.",
                "Cambios guardados con exito",
                JOptionPane.INFORMATION_MESSAGE);
        
        // 4. Navegar de vuelta al menú
        controlPantallas.mostrarMenuConductor();

    }//GEN-LAST:event_guardarCambiosViajeBtnActionPerformed

