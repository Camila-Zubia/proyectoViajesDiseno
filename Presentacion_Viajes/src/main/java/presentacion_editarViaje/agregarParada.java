    private void guardarParadaBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarParadaBtn1ActionPerformed
        try {
            String direccion = direccionTField.getText().trim();
            String precioStr = precioTField.getText().trim();

            // Validaciones
            if (direccion.isEmpty() || precioStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Direccion y precio son obligatorios.", "Campos Vacios", JOptionPane.WARNING_MESSAGE);
                return;
            }

            double precio;
            try {
                precio = Double.parseDouble(precioStr);
                if (precio <= 0) {
                    JOptionPane.showMessageDialog(this, "El precio debe ser un numero positivo.", "Dato Invalido", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El precio debe ser un número valido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Llama a la capa de control para guardar la parada temporal
            controlPantallas.agregarParadaEnEdicionTemporal(direccion, precio);

            // Regresa a la pantalla de edicion de paradas
           controlPantallas.mostrarEditarParadas();
           

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this,
                    "Error al guardar la parada: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_guardarParadaBtn1ActionPerformed


